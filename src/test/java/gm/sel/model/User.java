package gm.sel.model;

public class User {

	private String email;
	private String pass;

	public String getEmail() {
		return email;
	}
	public User setEmail(String email) {
		this.email = email;
		return this;
	}
	public String getPass() {
		return pass;
	}
	public User setPass(String pass) {
		this.pass = pass;
		return this;
	}
}
